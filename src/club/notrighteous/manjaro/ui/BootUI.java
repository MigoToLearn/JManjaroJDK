    private void setJdkBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setJdkBtnActionPerformed
        String selectedJdk = jdkVersionsBox.getItemAt(jdkVersionsBox.getSelectedIndex());
        if(selectedJdk.contains("default")) {
            InfoUI.displayError("JDK already default");
        } else {
            if(!selectedJdk.isEmpty()) {
                CommandUtil.setJdkDefault(selectedJdk);
            } else {
                InfoUI.displayError("Empty jdk selection");
            }
            populateJdks();
        }
    }//GEN-LAST:event_setJdkBtnActionPerformed

