    public void populateJdks() {
        ArrayList<String> jdks = CommandUtil.getJdkInstalled();
        jdkVersionsBox.removeAllItems();
        for(String jdk : jdks) {
            jdkVersionsBox.addItem(jdk);
        }
    }

